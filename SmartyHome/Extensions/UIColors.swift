import Foundation

import UIKit


extension UIColor {
  @nonobjc class var grayColor: UIColor {
    return UIColor(red: 142.0 / 255.0, green: 146.0 / 255.0, blue: 149.0 / 255.0, alpha: 1.0)
  }
  
  @nonobjc class var blueColor: UIColor {
    return UIColor(red: 4.0 / 255.0, green: 123.0 / 255.0, blue: 239.0 / 255.0, alpha: 1.0)
  }
  
  @nonobjc class var blackColor: UIColor {
    return UIColor(red: 13.0 / 255.0, green: 18.0 / 255.0, blue: 21.0 / 255.0, alpha: 1.0)
  }
    
  @nonobjc class var backgroundColor: UIColor {
    return UIColor(red: 248.0 / 255.0, green: 250.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
  }

}
